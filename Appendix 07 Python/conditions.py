a = 2
b = 3

# relational operators
print(a == b)
print(a != b)
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)

#Falsy values
#any empty data structure, None, 0

# logical operators, also short circuiting
print(1 and print("Hello World"))
print(1 or print("Hello World"))

# if , elif and else

name = "Jacob"
surname = "Daniel"

if (name and surname):
  print(name, surname)
elif (name):
  print(name)
elif (surname):
  print(surname)
else:
  print("name and surname are empty")

