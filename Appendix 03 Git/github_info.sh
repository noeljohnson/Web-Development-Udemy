## GTG

# following are the steps to make a local repo avaialble remotely in github

# login to your github account
# create a new repository (add repo name, description etc)
# get a link for the remote git repo, add it to the local gits remote branch using the following command
## git remote add <remote-name> <github-link>
# check if successfull by the following command
## git remote -v 
# (should have information about fetch: download and push: upload)
# then push upstream to <remote-name> by the following command
## git push -u <remote-name> <branch-name>

